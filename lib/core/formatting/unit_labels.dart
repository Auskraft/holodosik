/// Человекочитаемые подписи единиц измерения для выпадающего списка.
abstract final class UnitLabels {
  static const Map<String, String> _labels = {
    'г': 'граммы (г)',
    'кг': 'килограммы (кг)',
    'мл': 'миллилитры (мл)',
    'л': 'литры (л)',
    'ст.л.': 'столовые ложки',
    'ч.л.': 'чайные ложки',
    'стакан': 'стаканы (350 мл)',
    'шт': 'штуки',
    'щепотка': 'щепотки',
    'по вкусу': 'по вкусу',
    'пучок': 'пучки',
    'зубчик': 'зубчики',
    'веточка': 'веточки',
    'лист': 'листья',
    'банка': 'банки',
    'кусок': 'куски',
    'упак': 'упаковки',
    'см': 'сантиметры (см)',
    'м': 'метры (м)',
  };

  /// Единицы, для которых по умолчанию берём 1 (штучные), остальные — 100.
  static const Set<String> _ones = {
    'шт', 'пучок', 'зубчик', 'веточка', 'банка', 'кусок', 'лист',
    'по вкусу', 'щепотка', 'упак',
  };

  static String label(String unit) => _labels[unit] ?? unit;

  static double defaultAmount(String unit) => _ones.contains(unit) ? 1 : 100;
}
